//04 Type casting in java
/*
  widening casting
 
        byte -> short -> char -> int -> float -> long -> double
		
	Narrowing casting
	
		double -> long -> float-> int -> char -> short -> byte
*/
import java.lang.*;

class casting
{
public static void main(String args[])


{
	int a=10;
	double b=a;
	
	System.out.println("int  :"+a);
	System.out.println("double :"+b);
	
}
}
		
  
